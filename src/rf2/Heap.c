void writeInt2 (Ref addr, Long value) {
    writeInt(addr, value>>8);
    writeInt(addr+1, value);
}
void writeInt4 (Ref addr, Long value) {
    writeInt2(addr, value >> 16);
    writeInt2(addr+2, value);
}


